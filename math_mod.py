



def add(x, y):
    result = x + y
    return f'Result of adding {x} and {y} is {result}'

def subtract(x, y):
    result = x - y
    return f'Result of subtracting {x} and {y} is {result}'

def multiply(x, y):
    result = x * y
    return f'Result of multiplying {x} and {y} is {result}'

def divide(x, y):
    result = x / y
    return f'Result of dividing {x} and {y} is {result}'

